function maakSlider(container, slidernaam, plaatjes){
	$('.slider_div').remove(); // Verwijder alle sliders die al bestaan.

	var slider = $("<div>")
					.addClass('slider_div')
					.hide();
	var innerDiv = $('<div>')
					.attr('id', slidernaam)
					.addClass('image_carousel');
	for (var i = 0; i < plaatjes.length; i++) {
		var img = $('<img>')
					.attr('src', plaatjes[i].src)
					.attr('title', plaatjes[i].title)
					.attr('width', 196)
					.attr('height', 460);
		innerDiv.append(img);
	}

	var prev = $('<a>')
					.addClass('prev')
					.attr('href', '#')
					.attr('id', slidernaam + '_prev');
	var next = $('<a>')
					.addClass('next')
					.attr('href', '#')
					.attr('id', slidernaam + '_next');
	var pag = $('<div>')
					.addClass('pagination')
					.attr('id', slidernaam + '_pag');

	slider.append(innerDiv);
	slider.append(prev);
	slider.append(next);
	slider.append(pag);

	$(container).append(slider);

	/* Hierboven de opmaak en structuur van de slider */
	/* Hieronder de functionaliteit van de slider */

	$('#' + slidernaam).parent().show();
	$('#' + slidernaam).carouFredSel({
		circular: false,
		auto: false,
		infinite: false,
		items: 5,
		scroll: 5,
		next: {
			button: '#' + slidernaam + '_next'
		},
		prev: {
			button: '#' + slidernaam + '_prev'
		},
		pagination: '#' + slidernaam + '_pag'
	});
	$('#' + slidernaam + '_pag').css('margin-left', '-' + ($('#' + slidernaam + '_pag').width() / 2) + 'px');
	// Left van #slider_pag staat op 50% dus margin-left = -(helft van de breedte van #slider_pag) px om te centreren.

	$('#' + slidernaam + ' img').tooltip({
		content: function(){
			return $(this).attr('title');
		},
		position: {
			my: "center", 
			at: "center bottom-25%" 
		}
	});
}